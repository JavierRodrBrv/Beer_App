// Clase que representa un modelo de cerveza con propiedades específicas.
class Beer {
  // Nombre de la cerveza.
  final String name;

  // URL de la imagen de la cerveza.
  final String imageUrl;

  // Descripción de la cerveza.
  final String description;

  // Constructor que requiere el nombre, la URL de la imagen y la descripción.
  Beer({required this.name, required this.imageUrl, required this.description});
}

